// ASSIGNMENT OPERATOR:
let r = 5;
// the equal sign is the assignment operator

// ADDITION
r += 1; // r = r + 1 or 5 + 1
console.log(r);

// let something = "this";
// something += " and that";
// console.log(something);

// SUBTRACTION
r -= 2; // r = r - 2 or 6 -2
console.log(r);

// MULTIPLICATION
r *= 2; // r = r * 2 or 4 * 2
console.log(r);

// DIVISION
r /= 1; // r = r / 1 or 8 / 1
console.log(r);

// EXPONENTIATION
r **= 2; // 2 = r ** 2 or 8 ** 2
console.log(r);

// REMAINDER (MODULO)
r %= 1;
console.log(r);